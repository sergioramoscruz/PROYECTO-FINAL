    private void btneliminar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminar1ActionPerformed
        // TODO add your handling code here:
        if (txtidequipo.getText() != ("")) {

            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "SEGURO DE ELIMINAR?", "Confirmar", 2);

            if (confirmacion == 0) {
                repositoryEquipos func = new repositoryEquipos();
                equipo dts = new equipo();

                dts.setIdequipo(Integer.parseInt(txtidequipo.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();

            }

        }
    }//GEN-LAST:event_btneliminar1ActionPerformed

