    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        
        equipo dts = new equipo();
        repositoryEquipos func = new repositoryEquipos();

        dts.setNombre(txtnombreequipo.getText());
        dts.setOrigen(txtciudadorigen.getText());
        
         int seleccionado = cboserie.getSelectedIndex();
        dts.setSerie((String) cboserie.getItemAt(seleccionado));
        
         dts.setNum_jugadores(Integer.parseInt(txtnumjugadores.getText()));
       dts.setNum_asistentes(Integer.parseInt(txtnumasistentes.getText()));
        dts.setDetalles(txtdetalles.getText());
        
       dts.setCedula(Integer.parseInt(txtcedulatecnico.getText()));
       dts.setNombresApellidos(txtnombretecnico.getText());
       dts.setEdad(Integer.parseInt(txtedadtecnico.getText()));
        dts.setPasaporte(Integer.parseInt(txtpasaportetecnico.getText()));
        
      
        if (accion.equals("guardar")) {
            if (func.Grabar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "REGISTRO EXITOSO");
                mostrar("");
                inhabilitar();

            }

        } else if (accion.equals("editar")) {
            dts.setIdequipo(Integer.parseInt(txtidequipo.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "MODIFICACION EXITOSA");
                mostrar("");
                inhabilitar();
            }
        }
        
       
            
        
        
        
      //  agregarequipo();
        Limpiar();

    }//GEN-LAST:event_btnguardarActionPerformed

