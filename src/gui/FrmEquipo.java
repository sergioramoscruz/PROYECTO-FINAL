    public void llenarequipo() {
        List<equipo> equi = equiporep.getEquipo();
        DefaultTableModel rt = (DefaultTableModel) tablaequipos.getModel();
        Object equip[] = new Object[11];
        rt.setRowCount(0);//para que no llene una tras otra 
        for (equipo eq : equi) {
            equip[0] = eq.getIdequipo();
            equip[1] = eq.getNombre();
             equip[2] = eq.getOrigen();
            equip[3] = eq.getSerie();
              equip[4] = eq.getNum_jugadores();
              equip[5] = eq.getNum_asistentes(); 
             equip[6] = eq.getDetalles();
              equip[7] = eq.getCedula();
              equip[8] = eq.getNombresApellidos();   
              equip[9] = eq.getEdad();
              equip[10] = eq.getPasaporte();
                
            rt.addRow(equip);
        }
        tablaequipos.setModel(rt);
    }

