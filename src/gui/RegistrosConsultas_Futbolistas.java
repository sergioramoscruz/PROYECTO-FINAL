     public void Consultar(DefaultTableModel model){
        String sql = "";
        if(rcedula.isSelected()){
            sql = "SELECT jugador.id_jugador,jugador.nombres,jugador.apellidos,jugador.edad,jugador.pasaporte,jugador.posicion,jugador.equipo_debug,jugador.equipo_actual,jugador.cotizacion,jugador.cedula FROM jugador  WHERE jugador.cedula = '" + txtcedula.getText().toUpperCase()+ "'";
        }
        if(rpasaporte.isSelected()){
            sql = "SELECT jugador.id_jugador,jugador.nombres,jugador.apellidos,jugador.edad,jugador.pasaporte,jugador.posicion,jugador.equipo_debug,jugador.equipo_actual,jugador.cotizacion,jugador.cedula FROM jugador  WHERE jugador.pasaporte = '" + txtpasaporte.getText().toUpperCase()+ "'";
        }
        if(rposicion.isSelected()){
            sql = "SELECT jugador.id_jugador,jugador.nombres,jugador.apellidos,jugador.edad,jugador.pasaporte,jugador.posicion,jugador.equipo_debug,jugador.equipo_actual,jugador.cotizacion,jugador.cedula FROM jugador  WHERE jugador.posicion = '" + cboposicion.getSelectedItem().toString()+ "'";
        }
        int c = 0;
        Object [] tabla = new Object[10];
        
        Conexion con = new Conexion();
        try{
            Connection conex = con.Open();
            PreparedStatement pst = conex.prepareCall(sql);
            ResultSet rs = pst.executeQuery();
            while(rs.next()){
                c = 0;
                while(c < tabla.length){
                    tabla[c] = rs.getString(c+1);
                    c++;
                }
                model.addRow(tabla);
            }
            Tablafutbolistas.setModel(model);
        }
        catch(SQLException exc){
            JOptionPane.showMessageDialog(null,exc.getMessage(),"WARNING",JOptionPane.ERROR_MESSAGE);
        }
    }
    
     public int print(Graphics graphics, PageFormat pageFormat, int pageIndex)throws PrinterException{
          if (pageIndex==0){
            Graphics2D g2d=(Graphics2D) graphics;
            g2d.translate(pageFormat.getImageableX()+30,pageFormat.getImageableY()+30);
            jPanel1.printAll(graphics);
            return PAGE_EXISTS;
        }else{
            return NO_SUCH_PAGE;
            
}}
    
    
