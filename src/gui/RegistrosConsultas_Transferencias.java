    public void Consultar(DefaultTableModel model) {

        String sql = "";

        if (rfecha.isSelected()) {
            sql = "SELECT transferencia.id_transferencia,transferencia.fecha,transferencia.modo_transferencia,transferencia.equipo_transferencia,transferencia.descripcion,transferencia.cotizacion,transferencia.porcentaje,transferencia.id_jugador FROM transferencia WHERE transferencia.fecha = '" + txtfecha.getText().toUpperCase() + "'";
        }
        if (rmodo.isSelected()) {
            sql = "SELECT transferencia.id_transferencia,transferencia.fecha,transferencia.modo_transferencia,transferencia.equipo_transferencia,transferencia.descripcion,transferencia.cotizacion,transferencia.porcentaje,transferencia.id_jugador FROM transferencia WHERE transferencia.modo_transferencia = '" + cbomodo.getSelectedItem().toString() + "'";
        }
        int c = 0;
        Object[] tabla = new Object[8];

        Conexion conn = new Conexion();
        try {
            Connection conex = conn.Open();
            PreparedStatement pst = conex.prepareCall(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                c = 0;
                while (c < tabla.length) {
                    tabla[c] = rs.getString(c + 1);
                    c++;
                }
                model.addRow(tabla);
            }
            tablatransferencia.setModel(model);
        } catch (SQLException exc) {
            JOptionPane.showMessageDialog(null, exc.getMessage(), "WARNING", JOptionPane.ERROR_MESSAGE);
        }

    }

    public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
        if (pageIndex == 0) {
            Graphics2D g2d = (Graphics2D) graphics;
            g2d.translate(pageFormat.getImageableX() + 30, pageFormat.getImageableY() + 30);
            jPanel1.printAll(graphics);
            return PAGE_EXISTS;
        } else {
            return NO_SUCH_PAGE;

        }
    }

